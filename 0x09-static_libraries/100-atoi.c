#include <stdio.h>
#include <stdlib.h>

/**
 * _atoi - Invoke atoi() to convert string to integer
 *
 * @s: Character string parameter to get the input data
 *
 * Function - atoi(): Do the conversion
 *
 * _atoi: Convert input string to integer
 *
 * Return: rt (conversion of string)
 */

int _atoi(char *s)
{
	int rt = atoi(s);

	return (rt);
}
