#include "variadic_functions.h"

/**
 * _putchar - Function to print a single character
 *	(alphabet, numeric, special char, newline "\n"/10 and
 *	other single characters including escape characters:
 *	characters starting with  backslash '\')
 *	to the standard output
 *
 * @ch: Character parameter
 */

void _putchar(char ch)
{
	putchar (ch);
}
